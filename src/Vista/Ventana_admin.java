    private void Boton_GuardarCateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_GuardarCateActionPerformed
        // TODO add your handling code here:
        String Nombre_c=null, Descripcion=null;
        Nombre_c=NombreCatetxt.getText();
        Descripcion=Descripciontxt.getText();
        if(!Nombre_c.equals(null)){
            if(!Descripcion.equals(null)){
                if(in.insertarCategoria(Nombre_c, Descripcion)){
                     JOptionPane.showMessageDialog(rootPane, "Categoria Guardada");
                }else{
                    JOptionPane.showMessageDialog(rootPane, "La Categoria NO ha sido guardado");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Favor de ingresar la descripcion de la categoria!!");
            }
        }else{
             JOptionPane.showMessageDialog(rootPane, "Favor de ingresar el Nombre de la categoria!!");
        }
        limpiarCategoria();
        
    }//GEN-LAST:event_Boton_GuardarCateActionPerformed

    private void Boton_GuardarProveeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_GuardarProveeActionPerformed
        // TODO add your handling code here:
        String Nombre=null, Telefono=null, rfc=null;
        Nombre=Nombre_Proveedortxt.getText();
        Telefono=Telefono_Proveedortxt.getText();
        rfc=rfc_proveedortxt.getText();
        
        if(!Nombre.equals(null)){
            if(!Telefono.equals(null)){
                if(!rfc.equals(null)){
                    if(in.insertarProveedor(Nombre, Telefono, rfc))
                    {
                        JOptionPane.showMessageDialog(rootPane, "Proveedor Guardado");
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "El Proveedor no ha sido guardado");
                    }
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Favor de ingresar el rfc del proveedor!!");
                }
            }else
            {
                JOptionPane.showMessageDialog(rootPane, "Favor de ingresar el telefono del proveedor!!");
            }
        }else
        {
            JOptionPane.showMessageDialog(rootPane, "Favor de ingresar el nombre del proveedor!!");
        }
        limpiarProveedor();
    }//GEN-LAST:event_Boton_GuardarProveeActionPerformed

